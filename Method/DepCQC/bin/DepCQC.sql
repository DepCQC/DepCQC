CREATE TABLE IF NOT EXISTS violations (
                   id INT AUTO_INCREMENT PRIMARY KEY,
                   file_name VARCHAR(255),
                   beginline INT,
                   endline INT,
                   begincolumn INT,
                   endcolumn INT,
                   rule VARCHAR(255),
                   ruleset VARCHAR(255),
                   packages VARCHAR(255),
                   classes VARCHAR(255),
                   method VARCHAR(255),
                   variable VARCHAR(255),
                   external_info_url VARCHAR(255),
                   priority INT,
                   description TEXT,
                   example TEXT,
                   language VARCHAR(255),
                   repository_id VARCHAR(255),
                   project_id VARCHAR(255),
                   scan_time TIMESTAMP,
                   scan_executor VARCHAR(255),
                   scan_uuid VARCHAR(255),
                   minio_url TEXT,
                   s3_url TEXT,
                   code_snippet TEXT
);
